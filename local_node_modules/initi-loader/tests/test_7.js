export default function run() {
    const MESSAGES = {
        success: "Test 7: providers/system/main loaded successfully",
        error: "Test 7: providers/system/main failed to load",
    }
    return new Promise((resolve, reject) => {
        const deps = [
            "providers/system/main", Loader.Type.requirejs,
        ];

        console.debug("::Starting test_7.js::");
        loader.loadBulk(deps, () => {
            if (global.v2 === undefined || global.v2.tools === undefined) {
                console.error(MESSAGES.error);
                throw new Error(MESSAGES.error);
            }
            console.debug(MESSAGES.success);
            resolve();
        }, (err) => {
            console.error(MESSAGES.error, err);
            throw new Error(MESSAGES.error);
        });
    })
}
